package dev.halby.workout_tracker.ExerciseGroup;

public enum ExerciseGroup {
    // Upper Body
    CHEST,                   // Chest (Pectorals)
    UPPER_BACK,              // Upper Back (Trapezius, Rhomboids)
    MIDDLE_BACK,             // Middle Back (Latissimus)
    LOWER_BACK,              // Lower Back (Erector Spinae)
    SHOULDERS_FRONT,         // Front Shoulders (Anterior Deltoids)
    SHOULDERS_SIDE,          // Side Shoulders (Lateral Deltoids)
    SHOULDERS_REAR,          // Rear Shoulders (Posterior Deltoids)
    BICEPS,                  // Biceps
    TRICEPS,                 // Triceps
    FOREARMS,                // Forearms

    // Lower Body
    QUADRICEPS,              // Quadriceps (Front Thigh)
    HAMSTRINGS,              // Hamstrings (Back Thigh)
    GLUTES,                  // Glutes (Buttocks)
    CALVES,                  // Calves

    // Core
    ABS,                     // Abs (Abdominals)
    OBLIQUES,                // Obliques (Side Abdominals)
    LOWER_ABS,               // Lower Abs (Lower Abdominals)
    HIP_FLEXORS,             // Hip Flexors
    LOWER_BACK_CORE,         // Lower Back (Core)

    // Other
    NECK,                    // Neck
    TOTAL_BODY               // Total Body Exercises
}
